#include <stdio.h>

int main()
{

    int var1 = 10, var2 = 2, result = 0;

    printf("\nvar1 is %d and var2 is %d .", var1, var2);

    // addition of the two numbers

    result = var1 + var2;

    printf("\nAddtion of number is %d .", result);

    // multiplication of two numbers

    result = var1 * var2;

    printf("\nMultiplication is %d", result);

    // division of two number

    result = var1 / var2;

    printf("\nDivision is %d .", result);

    // Subtraction of two number

    result = var1 - var2;

    printf("\nSubtraction is %d .", result);

    //To see the remainder 

    result = var1 % var2 ;

    printf("\nRemainder is %d .\n",result);
}